# else with while loop
'''
Essentially what the else statement does is that as soon as the while loop condition becomes False,
 the interpreter comes out of the while loop and the else statement is executed
'''
x = 4
while(x > 0):
    print(x)
    x = x - 1
else:
    print(x)


